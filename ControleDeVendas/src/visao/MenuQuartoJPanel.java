    private void setCabecalho() {
        cabecalhoQuartojTextField.setText(String.valueOf(quarto.getNumero()));
        cabecalhoQuartojTextField.setEditable(false);
        cabecalhoQuartojTextField.setEnabled(false);
    }

    private void setListasDeProdutos() {
        
        modeloTabelaProdutosAPagar = (DefaultTableModel) this.tabelaProdutosAPagar.getModel();
        modeloTabelaProdutosPagos = (DefaultTableModel) this.tabelaProdutosPagos.getModel();
       
        if( quarto.listaProdutosAderidos != null)
        {
            for (Produto produto : quarto.listaProdutosAderidos) {
                this.modeloTabelaProdutosAPagar.addRow(produto.getArrayStringDeDadosComDataAdesao());
            }
        }
        
        if( quarto.listaProdutosPagos != null)
        {
            for (Produto produto : quarto.listaProdutosPagos) {
                this.modeloTabelaProdutosPagos.addRow(produto.getArrayStringDeDadosComDataPagamento());
            }
        }      
    }
   public void adicionarCarrinho(ArrayList<Produto> listaDeProdutosDoCarrinho){      
            for (Produto produto : listaDeProdutosDoCarrinho) {
            quarto.addProdutoAoQuarto(produto);
            modeloTabelaProdutosAPagar.addRow(produto.getArrayStringDeDadosComDataAdesao());
        }
   }
