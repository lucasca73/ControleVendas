    private void pagarProdutojButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarProdutojButtonActionPerformed
        
        String nomeProduto =(String) modeloTabelaProdutosAPagar.getValueAt(tabelaProdutosAPagar.getSelectedRow(),1);

        Produto produtoApagar = quarto.getProdutoAderidoPorNome(nomeProduto);
        
        //System.out.println("TRETA" + produtoApagar.getNome());
        quarto.pagarProduto(produtoApagar);
        modeloTabelaProdutosAPagar.removeRow(tabelaProdutosAPagar.getSelectedRow());
        modeloTabelaProdutosPagos.addRow(produtoApagar.getArrayStringDeDadosComDataPagamento());
        
        pagarProdutojButton.setEnabled(false);
        
    }//GEN-LAST:event_pagarProdutojButtonActionPerformed

    private void tabelaProdutosAPagarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdutosAPagarMouseClicked
        int produtoSelecionado = tabelaProdutosAPagar.getSelectedRow();
        
        if(produtoSelecionado >= 0 && produtoSelecionado <= tabelaProdutosAPagar.getRowCount()){
            pagarProdutojButton.setEnabled(true);
        }
        else
           System.out.println("Produto nao encontrado");
    }//GEN-LAST:event_tabelaProdutosAPagarMouseClicked


